#pragma once

#include "command.h"

//-----------------------------------------------------------------------------
// A HistoryCommand object has the sole function of displaying a Patron's
// history.  It has the character 'H' as its command call, and requires a
// Patron object in order to execute.
class HistoryCommand : public Command {
		
	public:

//***************************************************************HistoryCommand()***********************************************************************
// Default constructor
//******************************************************************************************************************************************************
		HistoryCommand();


//***************************************************************~HistoryCommand()***********************************************************************
// Destructor
//******************************************************************************************************************************************************
		~HistoryCommand();


	
//************************************************************HistoryCommand::execute()*****************************************************************
// is a bool pure virtual function from class command returns true when displaying the history of an specific account specified by the read Patron ID
// from file
//******************************************************************************************************************************************************
		virtual bool execute(ItemTree* [], Patron* [], istream&);


//******************************************************************create()****************************************************************************
// Creates and return a Command* to a new history command object
//******************************************************************************************************************************************************
		virtual Command* create();
};

