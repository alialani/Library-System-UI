#include "item.h"


//****************************************************************Item()********************************************************************************
// Default constructor
//******************************************************************************************************************************************************
Item::Item() {}


//****************************************************************~Item()********************************************************************************
// Destructor
//******************************************************************************************************************************************************
Item::~Item() {}


//*************************************************************getAvailable()***************************************************************************
// Pure virtual method return int available
//******************************************************************************************************************************************************
int Item::getAvailable() {
	return available;
}


//***************************************************************checkout()*****************************************************************************
// Decrease available by 1 and return true for that
// Used in the checkout command excute method
//******************************************************************************************************************************************************
bool Item::checkout() { 
	if(available > 0) {
		available = available-1; 
		return true;
	} 
	return false;
} 


//***************************************************************returnItem()**************************************************************************
// Increase available by 1 and return true for that
// Used in the return command excute method
//******************************************************************************************************************************************************
bool Item::returnItem() {
	available = available+1; 
	return true;
}


